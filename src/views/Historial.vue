  <v-container fluid class="py-8 bg-surface">
    <div class="view-shell">
      <v-card class="card card--hover history-card mb-6">
        <div class="history-header pa-6">
          <h2 class="text-h5 font-weight-bold mb-0">Historial</h2>
        </div>

      <!-- Error Alert -->
      <v-alert
        v-if="error"
        type="error"
        variant="tonal"
        closable
        class="mb-4"
        @click:close="error = null"
      >
        {{ error }}
      </v-alert>

      <!-- Success Snackbar -->
      <v-snackbar
        v-model="snackbar.show"
        :color="snackbar.color"
        :timeout="3000"
      >
        {{ snackbar.message }}
        <template #actions>
          <v-btn
            icon="mdi-close"
            size="small"
            @click="snackbar.show = false"
          />
        </template>
      </v-snackbar>

        <div class="filters-section pa-4">
          <div class="d-flex align-center justify-space-between mb-3">
            <h4 class="text-subtitle-1 font-weight-bold text-white mb-0">
              <v-icon size="small" class="mr-1">mdi-filter-outline</v-icon>
              Filtros y orden
            </h4>
            <v-btn
              variant="tonal"
              size="small"
              class="btn-pill text-body-2 font-weight-medium"
              @click="showFilters = !showFilters"
            >
              <v-icon size="small" class="mr-1">
                {{ showFilters ? 'mdi-chevron-up' : 'mdi-chevron-down' }}
              </v-icon>
              {{ showFilters ? 'Ocultar' : 'Mostrar' }}
            </v-btn>
          </div>
          <v-expand-transition>
            <div v-show="showFilters">
              <v-row dense>
                <v-col cols="12" md="4">
                  <label class="app-input-label filter-label" for="history-sort">Ordenar por</label>
                  <v-select
                    id="history-sort"
                    v-model="filters.sort_by"
                    :items="sortOptions"
                    density="comfortable"
                    hide-details
                    class="app-input"
                    @update:model-value="fetchPurchases"
                  />
                </v-col>
                <v-col cols="12" md="4">
                  <label class="app-input-label filter-label" for="history-order">Orden</label>
                  <v-select
                    id="history-order"
                    v-model="filters.order"
                    :items="orderOptions"
                    density="comfortable"
                    hide-details
                    class="app-input"
                    @update:model-value="fetchPurchases"
                  />
                </v-col>
                <v-col cols="12" md="4">
                  <label class="app-input-label filter-label" for="history-per-page">Resultados por página</label>
                  <v-select
                    id="history-per-page"
                    v-model="filters.per_page"
                    :items="perPageOptions"
                    density="comfortable"
                    hide-details
                    class="app-input"
                    @update:model-value="onPerPageChange"
                  />
                </v-col>
              </v-row>
            </div>
          </v-expand-transition>
        </div>

        <div v-if="loading || purchases.length === 0" class="history-body pa-6">
          <div v-if="loading" class="text-center py-12">
            <v-progress-circular
              indeterminate
              color="primary"
              size="64"
            />
            <p class="text-body-1 mt-4">Cargando historial...</p>
          </div>
          <div v-else class="text-center py-12">
            <v-icon size="80" color="grey-lighten-2" class="mb-4">
              mdi-cart-outline
            </v-icon>
            <h2 class="text-h6 font-weight-bold mb-2">No hay compras registradas</h2>
            <p class="text-body-2 text-medium-emphasis mb-5">
              Las compras que realices aparecerán aquí
            </p>
            <v-btn
              color="primary"
              prepend-icon="mdi-view-list"
              to="/lists"
              class="btn-pill text-body-2 font-weight-medium"
            >
              Ir a mis listas
            </v-btn>
          </div>
        </div>
      </v-card>

      <!-- Purchases List -->
      <div v-if="!loading && purchases.length > 0">
        <v-row>
          <v-col
            v-for="purchase in purchases"
            :key="purchase.id"
            cols="12"
          >
            <v-card class="card card--hover">
              <v-card-text class="pa-4">
                <v-row align="center">
                  <!-- Purchase Info -->
                  <v-col cols="12" md="6">
                    <div class="d-flex align-center mb-2">
                      <v-icon color="primary" class="mr-2">mdi-cart-check</v-icon>
                      <h3 class="text-h6 font-weight-bold">
                        {{ purchase.list?.name || 'Lista sin nombre' }}
                      </h3>
                    </div>
                    <p class="text-body-2 text-medium-emphasis mb-1">
                      <v-icon size="small" class="mr-1">mdi-calendar</v-icon>
                      {{ formatDate(purchase.createdAt) }}
                    </p>
                    <p v-if="purchase.list?.description" class="text-body-2 text-medium-emphasis mb-1">
                      {{ purchase.list.description }}
                    </p>
                    <p class="text-body-2 text-medium-emphasis">
                      <v-icon size="small" class="mr-1">mdi-package-variant</v-icon>
                      {{ purchase.items?.length || 0 }} productos comprados
                    </p>
                  </v-col>

                  <!-- Actions -->
                  <v-col cols="12" md="6" class="text-md-right">
                    <v-btn
                      color="primary"
                      variant="outlined"
                      class="btn-pill text-body-2 font-weight-medium mr-2"
                      prepend-icon="mdi-eye"
                      @click="viewPurchaseDetails(purchase)"
                    >
                      Ver detalles
                    </v-btn>
                    <v-btn
                      color="primary"
                      variant="flat"
                      class="btn-pill text-body-2 font-weight-medium"
                      prepend-icon="mdi-restore"
                      :loading="restoringId === purchase.id"
                      @click="confirmRestore(purchase)"
                    >
                      Restaurar
                    </v-btn>
                  </v-col>
                </v-row>
              </v-card-text>
            </v-card>
          </v-col>
        </v-row>

        <!-- Pagination -->
        <div v-if="pagination.totalPages > 1" class="mt-6">
          <div class="d-flex justify-center">
            <v-pagination
              v-model="pagination.currentPage"
              :length="pagination.totalPages"
              :total-visible="7"
              @update:model-value="onPageChange"
            />
          </div>
          <p class="text-center text-body-2 text-medium-emphasis mt-2">
            Mostrando {{ startItem }} - {{ endItem }} de {{ pagination.totalItems }} compras
          </p>
        </div>
      </div>

      <!-- Purchase Details Dialog -->
      <v-dialog v-model="detailsDialog.open" max-width="800" scrollable>
        <v-card v-if="detailsDialog.purchase">
          <v-card-title class="pa-4 d-flex align-center justify-space-between bg-primary">
            <div class="text-white">
              <h2 class="text-h6">{{ detailsDialog.purchase.list?.name || 'Detalles de Compra' }}</h2>
              <p class="text-body-2 mt-1">{{ formatDate(detailsDialog.purchase.createdAt) }}</p>
            </div>
            <v-btn
              icon="mdi-close"
              variant="text"
              color="white"
              @click="detailsDialog.open = false"
            />
          </v-card-title>

          <v-divider />

          <v-card-text class="pa-4" style="max-height: 500px;">
            <!-- Purchase metadata -->
            <div v-if="detailsDialog.purchase.metadata" class="mb-4">
              <h3 class="text-subtitle-1 font-weight-bold mb-2">Información adicional</h3>
              <pre class="text-body-2">{{ JSON.stringify(detailsDialog.purchase.metadata, null, 2) }}</pre>
            </div>

            <!-- Items list -->
            <h3 class="text-subtitle-1 font-weight-bold mb-3">
              Productos ({{ detailsDialog.purchase.items?.length || 0 }})
            </h3>
            <v-list v-if="detailsDialog.purchase.items?.length > 0" density="compact">
              <v-list-item
                v-for="item in detailsDialog.purchase.items"
                :key="item.id"
                class="px-0"
              >
                <template #prepend>
                  <v-icon color="success" class="mr-3">mdi-check-circle</v-icon>
                </template>
                <v-list-item-title class="font-weight-medium">
                  {{ item.product?.name || item.productName || 'Producto' }}
                </v-list-item-title>
                <v-list-item-subtitle>
                  {{ item.quantity }} {{ item.unit }}
                  <span v-if="item.product?.category?.name" class="ml-2">
                    • {{ item.product.category.name }}
                  </span>
                </v-list-item-subtitle>
              </v-list-item>
            </v-list>
            <p v-else class="text-body-2 text-medium-emphasis">
              No hay productos en esta compra
            </p>
          </v-card-text>

          <v-divider />

          <v-card-actions class="pa-4">
            <v-spacer />
            <v-btn
              variant="text"
              class="btn-rounded"
              @click="detailsDialog.open = false"
            >
              Cerrar
            </v-btn>
            <v-btn
              color="primary"
              variant="flat"
              class="btn-rounded"
              prepend-icon="mdi-restore"
              :loading="restoringId === detailsDialog.purchase.id"
              @click="confirmRestore(detailsDialog.purchase)"
            >
              Restaurar esta compra
            </v-btn>
          </v-card-actions>
        </v-card>
      </v-dialog>

      <!-- Restore Confirmation Dialog -->
      <v-dialog v-model="restoreDialog.open" max-width="500">
        <v-card>
          <v-card-title class="pa-4">
            <v-icon color="primary" class="mr-2">mdi-restore</v-icon>
            Restaurar Compra
          </v-card-title>
          <v-card-text class="pa-4">
            <p class="mb-3">
              ¿Deseas restaurar esta compra como una nueva lista de compras?
            </p>
            <p class="text-body-2 text-medium-emphasis">
              Se creará una nueva lista con el nombre:
              <strong>{{ restoreDialog.purchase?.list?.name }}</strong>
            </p>
          </v-card-text>
          <v-card-actions class="pa-4">
            <v-spacer />
            <v-btn
              variant="text"
              class="btn-rounded"
              @click="restoreDialog.open = false"
            >
              Cancelar
            </v-btn>
            <v-btn
              color="primary"
              variant="flat"
              class="btn-rounded"
              :loading="restoreDialog.loading"
              @click="executeRestore"
            >
              Restaurar
            </v-btn>
          </v-card-actions>
        </v-card>
      </v-dialog>
    </div>
  </v-container>
